import {
  readFileSync,
  writeFileSync,
  mkdirSync,
  rmSync,
  existsSync,
} from "node:fs";

// =====================================================
// jana-pecah-seed.mjs
// 1) Jana semula supabase/migrations/0017_seed_hasil_bulanan.sql
//    (logik sama dengan scripts/seed-hasil-supabase.mjs)
// 2) Pecahkan kepada fail kecil dalam supabase/seed-parts/
//    supaya boleh paste ke Supabase SQL Editor tanpa timeout.
//
// Guna:  node scripts/jana-pecah-seed.mjs [bil_setiap_fail]
//        (default 2000 pernyataan setiap fail)
// =====================================================

const failJson = "src/data/hasil-bulanan.json";
const STATEMEN_SEPART = Number(process.argv[2] || 2000);

const json = JSON.parse(readFileSync(failJson, "utf8"));
const semuaBulan = json.bulan;

const num = (v) => {
  const n = typeof v === "number" ? v : parseFloat(v);
  return Number.isFinite(n) ? n : 0;
};

function adalahBarisSah(p) {
  const nama = (p?.nama ?? "").toString().trim();
  if (!nama) return false;
  if (
    p?.pol_pn &&
    nama.toUpperCase() === p.pol_pn.toString().trim().toUpperCase()
  )
    return false;
  if (/^\d+(\.\d+)?$/.test(nama)) return false;
  return true;
}

function normalNama(nama) {
  return nama
    .toString()
    .toUpperCase()
    .replace(/\bFASA\s+III\b/g, "FASA 3")
    .replace(/\bFASA\s+II\b/g, "FASA 2")
    .replace(/\bFASA\s+I\b/g, "FASA 1")
    .replace(/\s+/g, " ")
    .trim();
}

function escSql(v) {
  if (typeof v === "string") return `'${v.replace(/'/g, "''")}'`;
  if (v === null || v === undefined) return 0;
  return v;
}

const COLS =
  "kod_bulan, nama_bulan, jenis, pol_pn, bil, nama, peserta, luas_hek, luas_operasi, hasil, hasil_per_hek, matlamat_setahun, pct_setahun, pendapatan, kos, untung_rugi";
const SET =
  "peserta = excluded.peserta, luas_hek = excluded.luas_hek, luas_operasi = excluded.luas_operasi, hasil = excluded.hasil, hasil_per_hek = excluded.hasil_per_hek, matlamat_setahun = excluded.matlamat_setahun, pct_setahun = excluded.pct_setahun, pendapatan = excluded.pendapatan, kos = excluded.kos, untung_rugi = excluded.untung_rugi, bil = excluded.bil, dikemaskini_pada = now()";

let jumlahDibuang = 0;

function prosesProjek(senarai, jenis) {
  if (!Array.isArray(senarai)) return [];
  const dilihat = new Set();
  const bersih = [];
  for (const p of senarai) {
    if (!adalahBarisSah(p)) {
      jumlahDibuang++;
      continue;
    }
    const kunci = normalNama(p.nama);
    if (dilihat.has(kunci)) {
      jumlahDibuang++;
      continue;
    }
    dilihat.add(kunci);
    const luasOperasi =
      jenis === "sawit" ? num(p.luas_dituai) : num(p.luas_ditoreh);
    const hasil = jenis === "sawit" ? num(p.hasil_mt) : num(p.hasil_kg);
    const matlamat = num(p.matlamat_setahun);
    const hasilPerHek = luasOperasi > 0 ? hasil / luasOperasi : 0;
    const pctSetahun = matlamat > 0 ? (hasil / matlamat) * 100 : 0;
    bersih.push({
      pol_pn: p.pol_pn || "",
      nama: p.nama,
      peserta: num(p.peserta),
      luas_hek: num(p.luas_hek),
      luas_operasi: luasOperasi,
      hasil,
      hasil_per_hek: hasilPerHek,
      matlamat_setahun: matlamat,
      pct_setahun: pctSetahun,
      pendapatan: num(p.pendapatan),
      kos: num(p.kos),
      untung_rugi: num(p.untung_rugi),
    });
  }
  // Jana semula bil 1..n bagi SETIAP kumpulan POL/PN (selaras laluan upload)
  const kiraan = {};
  bersih.forEach((r) => {
    kiraan[r.pol_pn] = (kiraan[r.pol_pn] || 0) + 1;
    r.bil = kiraan[r.pol_pn];
  });
  return bersih;
}

const statemen = [];
for (const bulan of semuaBulan) {
  const kod = bulan.kod_bulan || bulan.kod;
  for (const jenis of ["sawit", "getah"]) {
    const bersih = prosesProjek(bulan[jenis], jenis);
    for (const r of bersih) {
      const vals = [
        kod,
        bulan.nama,
        jenis,
        r.pol_pn,
        r.bil,
        r.nama,
        r.peserta,
        r.luas_hek,
        r.luas_operasi,
        r.hasil,
        r.hasil_per_hek,
        r.matlamat_setahun,
        r.pct_setahun,
        r.pendapatan,
        r.kos,
        r.untung_rugi,
      ]
        .map(escSql)
        .join(", ");
      statemen.push(
        `insert into public.hasil_bulanan (${COLS}) values (${vals}) on conflict (kod_bulan, jenis, nama) do update set ${SET};`
      );
    }
  }
}

const header =
  "-- Seed data for hasil_bulanan table\n" +
  "-- Generated by jana-pecah-seed.mjs\n" +
  "-- Nilai terbitan dikira semula; baris sampah ditapis; pendua dibuang; bil dijana semula per POL/PN.\n\n";

// 1) Fail penuh (regenerate 0017)
writeFileSync(
  "supabase/migrations/0017_seed_hasil_bulanan.sql",
  header + statemen.join("\n") + "\n"
);

// 2) Pecahan
const dir = "supabase/seed-parts";
if (existsSync(dir)) rmSync(dir, { recursive: true, force: true });
mkdirSync(dir, { recursive: true });
const jumBahagian = Math.ceil(statemen.length / STATEMEN_SEPART);
for (let i = 0; i < jumBahagian; i++) {
  const chunk = statemen.slice(i * STATEMEN_SEPART, (i + 1) * STATEMEN_SEPART);
  const no = String(i + 1).padStart(2, "0");
  const body =
    `-- Bahagian ${i + 1}/${jumBahagian} (${chunk.length} pernyataan)\n` +
    "begin;\n" +
    chunk.join("\n") +
    "\ncommit;\n";
  writeFileSync(`${dir}/0017_part_${no}.sql`, body);
}

console.log(
  `\u2713 Dijana ${statemen.length} pernyataan insert (${jumlahDibuang} baris sampah/pendua ditapis)`
);
console.log(`\u2713 0017_seed_hasil_bulanan.sql dikemaskini`);
console.log(
  `\u2713 Dipecah kepada ${jumBahagian} fail dalam ${dir}/ (max ${STATEMEN_SEPART} pernyataan setiap fail)`
);
