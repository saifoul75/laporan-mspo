import { readFileSync, writeFileSync } from "node:fs";

const failJson = "src/data/hasil-bulanan.json";
const json = JSON.parse(readFileSync(failJson, "utf8"));
const semuaBulan = json.bulan;

let sql = "-- Seed data for hasil_bulanan table\n";
sql += "-- Generated by seed-hasil-supabase.mjs\n\n";

for (const bulan of semuaBulan) {
  for (const p of bulan.sawit) {
    const vals = [
      bulan.kod_bulan || bulan.kod,
      bulan.nama,
      'sawit',
      p.pol_pn,
      p.bil,
      p.nama,
      p.peserta || 0,
      p.luas_hek || 0,
      p.luas_dituai || 0,
      p.hasil_mt || 0,
      p.mtan_hek || 0,
      p.matlamat_setahun || 0,
      p.pct_setahun || 0,
      p.pendapatan || 0,
      p.kos || 0,
      p.untung_rugi || 0,
    ].map(v => {
      if (typeof v === 'string') return `'${v.replace(/'/g, "''")}'`;
      return v;
    }).join(', ');
    
    sql += `insert into public.hasil_bulanan (kod_bulan, nama_bulan, jenis, pol_pn, bil, nama, peserta, luas_hek, luas_operasi, hasil, hasil_per_hek, matlamat_setahun, pct_setahun, pendapatan, kos, untung_rugi) values (${vals}) on conflict (kod_bulan, jenis, nama) do update set peserta = excluded.peserta, luas_hek = excluded.luas_hek, luas_operasi = excluded.luas_operasi, hasil = excluded.hasil, hasil_per_hek = excluded.hasil_per_hek, matlamat_setahun = excluded.matlamat_setahun, pct_setahun = excluded.pct_setahun, pendapatan = excluded.pendapatan, kos = excluded.kos, untung_rugi = excluded.untung_rugi, dikemaskini_pada = now();\n`;
  }
  
  for (const p of bulan.getah) {
    const vals = [
      bulan.kod_bulan || bulan.kod,
      bulan.nama,
      'getah',
      p.pol_pn,
      p.bil,
      p.nama,
      p.peserta || 0,
      p.luas_hek || 0,
      p.luas_ditoreh || 0,
      p.hasil_kg || 0,
      p.kg_hek || 0,
      p.matlamat_setahun || 0,
      p.pct_setahun || 0,
      p.pendapatan || 0,
      p.kos || 0,
      p.untung_rugi || 0,
    ].map(v => {
      if (typeof v === 'string') return `'${v.replace(/'/g, "''")}'`;
      return v;
    }).join(', ');
    
    sql += `insert into public.hasil_bulanan (kod_bulan, nama_bulan, jenis, pol_pn, bil, nama, peserta, luas_hek, luas_operasi, hasil, hasil_per_hek, matlamat_setahun, pct_setahun, pendapatan, kos, untung_rugi) values (${vals}) on conflict (kod_bulan, jenis, nama) do update set peserta = excluded.peserta, luas_hek = excluded.luas_hek, luas_operasi = excluded.luas_operasi, hasil = excluded.hasil, hasil_per_hek = excluded.hasil_per_hek, matlamat_setahun = excluded.matlamat_setahun, pct_setahun = excluded.pct_setahun, pendapatan = excluded.pendapatan, kos = excluded.kos, untung_rugi = excluded.untung_rugi, dikemaskini_pada = now();\n`;
  }
}

writeFileSync("supabase/migrations/0017_seed_hasil_bulanan.sql", sql);
console.log(`✓ Generated ${sql.split('\n').length - 2} insert statements`);
console.log("Run this via Supabase Dashboard SQL Editor or supabase db push");
