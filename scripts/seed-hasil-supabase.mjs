import { readFileSync, writeFileSync } from "node:fs";

const failJson = "src/data/hasil-bulanan.json";
const json = JSON.parse(readFileSync(failJson, "utf8"));
const semuaBulan = json.bulan;

// --- Penolong ---

// Tukar nilai kepada nombor yang selamat (0 jika tak sah)
const num = (v) => {
  const n = typeof v === "number" ? v : parseFloat(v);
  return Number.isFinite(n) ? n : 0;
};

// Tapis baris sampah: nama kosong, nama === pol_pn, atau nama nombor sahaja
function adalahBarisSah(p) {
  const nama = (p?.nama ?? "").toString().trim();
  if (!nama) return false;
  if (
    p?.pol_pn &&
    nama.toUpperCase() === p.pol_pn.toString().trim().toUpperCase()
  )
    return false;
  if (/^\d+(\.\d+)?$/.test(nama)) return false;
  return true;
}

// Normalisasi nama untuk kesan pendua (FASA II === FASA 2, ruang berganda, dll)
function normalNama(nama) {
  return nama
    .toString()
    .toUpperCase()
    .replace(/\bFASA\s+III\b/g, "FASA 3")
    .replace(/\bFASA\s+II\b/g, "FASA 2")
    .replace(/\bFASA\s+I\b/g, "FASA 1")
    .replace(/\s+/g, " ")
    .trim();
}

// Escape nilai untuk SQL
function escSql(v) {
  if (typeof v === "string") return `'${v.replace(/'/g, "''")}'`;
  if (v === null || v === undefined) return 0;
  return v;
}

const COLS =
  "kod_bulan, nama_bulan, jenis, pol_pn, bil, nama, peserta, luas_hek, luas_operasi, hasil, hasil_per_hek, matlamat_setahun, pct_setahun, pendapatan, kos, untung_rugi";
const SET =
  "peserta = excluded.peserta, luas_hek = excluded.luas_hek, luas_operasi = excluded.luas_operasi, hasil = excluded.hasil, hasil_per_hek = excluded.hasil_per_hek, matlamat_setahun = excluded.matlamat_setahun, pct_setahun = excluded.pct_setahun, pendapatan = excluded.pendapatan, kos = excluded.kos, untung_rugi = excluded.untung_rugi, bil = excluded.bil, dikemaskini_pada = now()";

let jumlahInsert = 0;
let jumlahDibuang = 0;

// Proses satu senarai projek (sawit/getah) bagi satu bulan:
// tapis sampah -> buang pendua -> kira nilai terbitan -> jana semula bil
function prosesProjek(senarai, jenis) {
  if (!Array.isArray(senarai)) return [];
  const dilihat = new Set();
  const bersih = [];

  for (const p of senarai) {
    if (!adalahBarisSah(p)) {
      jumlahDibuang++;
      continue;
    }
    const kunci = normalNama(p.nama);
    if (dilihat.has(kunci)) {
      jumlahDibuang++;
      continue;
    }
    dilihat.add(kunci);

    const luasOperasi =
      jenis === "sawit" ? num(p.luas_dituai) : num(p.luas_ditoreh);
    const hasil = jenis === "sawit" ? num(p.hasil_mt) : num(p.hasil_kg);
    const matlamat = num(p.matlamat_setahun);

    // Kira semula nilai terbitan (jangan bahagi dengan 0)
    const hasilPerHek = luasOperasi > 0 ? hasil / luasOperasi : 0;
    const pctSetahun = matlamat > 0 ? (hasil / matlamat) * 100 : 0;

    bersih.push({
      pol_pn: p.pol_pn || "",
      nama: p.nama,
      peserta: num(p.peserta),
      luas_hek: num(p.luas_hek),
      luas_operasi: luasOperasi,
      hasil,
      hasil_per_hek: hasilPerHek,
      matlamat_setahun: matlamat,
      pct_setahun: pctSetahun,
      pendapatan: num(p.pendapatan),
      kos: num(p.kos),
      untung_rugi: num(p.untung_rugi),
    });
  }

  // Jana semula bil berturutan 1..n
  bersih.forEach((r, i) => {
    r.bil = i + 1;
  });
  return bersih;
}

let sql = "-- Seed data for hasil_bulanan table\n";
sql += "-- Generated by seed-hasil-supabase.mjs\n";
sql +=
  "-- Nilai terbitan (hasil_per_hek, pct_setahun) dikira semula; baris sampah ditapis; pendua dibuang; bil dijana semula.\n\n";

for (const bulan of semuaBulan) {
  const kod = bulan.kod_bulan || bulan.kod;
  for (const jenis of ["sawit", "getah"]) {
    const bersih = prosesProjek(bulan[jenis], jenis);
    for (const r of bersih) {
      const vals = [
        kod,
        bulan.nama,
        jenis,
        r.pol_pn,
        r.bil,
        r.nama,
        r.peserta,
        r.luas_hek,
        r.luas_operasi,
        r.hasil,
        r.hasil_per_hek,
        r.matlamat_setahun,
        r.pct_setahun,
        r.pendapatan,
        r.kos,
        r.untung_rugi,
      ]
        .map(escSql)
        .join(", ");

      sql += `insert into public.hasil_bulanan (${COLS}) values (${vals}) on conflict (kod_bulan, jenis, nama) do update set ${SET};\n`;
      jumlahInsert++;
    }
  }
}

writeFileSync("supabase/migrations/0017_seed_hasil_bulanan.sql", sql);
console.log(
  `\u2713 Dijana ${jumlahInsert} pernyataan insert (${jumlahDibuang} baris sampah/pendua ditapis)`
);
console.log("Jalankan via Supabase Dashboard SQL Editor atau supabase db push");
